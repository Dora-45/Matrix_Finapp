def seed() -> None:
    db = SessionLocal()
    try:
        existing_keys = {
            (c.section, c.direction, c.article)
            for c in db.query(CashflowCategory).all()
        }
        added = 0

        for section, direction, article, keywords, account_filter, priority in RULES:
            key = (section, direction, article)
            if key in existing_keys:
                continue

            db.add(
                CashflowCategory(
                    section=section,
                    direction=direction,
                    article=article,
                    keywords=keywords,
                    account_type_filter=account_filter,
                    priority=priority,
                    is_active=True,
                )
            )
            added += 1

        db.commit()
        print(f"✅ Добавлено правил классификации: {added}")
        print(f"ℹ️  Всего правил в базе: {len(existing_keys) + added}")
    finally:
        db.close()


if __name__ == "__main__":
    seed()